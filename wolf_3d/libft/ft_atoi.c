#include "libft.h"

int		ft_atoi(const char *s)
{
	unsigned long int	res;
	int					i;
	int					si;

	i = 0;
	si = 1;
	res = 0;
	while (s[i] > 0 && s[i] != '\e' && s[i] <= ' ')
		i++;
	if (s[i] == '-')
		si = -1;
	if (s[i] == '-' || s[i] == '+')
		i++;
	while (s[i] >= '0' && s[i] <= '9')
	{
		res = res * 10 + (s[i] - '0');
		i++;
		if (res > 9223372036854775807 && si == 1)
			return (-1);
		else if (res >= 9223372036854775807 && si == -1)
			return (0);
	}
	return ((int)res * si);
}
